# Exercise 7: Convert Float to Integer and Vice Versa

# ---Part1: Convert Float to Integer---
initial_float = 9.75
result_int = int(initial_float)
# Print the integer and it's type
print(f"Float to Int: {result_int}, Type: {type(result_int)}")

# ---Part2: Convert integer to Float---
initial_int = 50
result_float = float(initial_int)
# Print the Float and it's type
print(f"Int to Float: {result_float}, Type: {type(result_float)}")