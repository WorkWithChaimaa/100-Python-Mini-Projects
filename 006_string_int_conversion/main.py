# Exercise 6: Convert String to Integer and Vice Versa

# --- Part 1: Convert string to integer ---
initial_str = "100"
result_int = int(initial_str)

# Print the integer result and its type
print(f"Integer value: {result_int}, Type: {type(result_int)}")

# --- Part 2: Convert integer to string ---
initial_int = 42
result_str = str(initial_int)

# Print the string result and its type
print(f"String value: {result_str}, Type: {type(result_str)}")