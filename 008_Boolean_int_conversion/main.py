# Exercise 8: Convert a Boolean to an Integer

# --- Part 1: Convert the Boolean value True into integer ---
bool_true = True
int_from_true = int(bool_true)
print(f"{bool_true} as an integer: {int_from_true}")

# --- Part 2: Convert the Boolean value False into integer ---
bool_false = False
int_from_false = int(bool_false)
print(f"{bool_false} as an integer: {int_from_false}")