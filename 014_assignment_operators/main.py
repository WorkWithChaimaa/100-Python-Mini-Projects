# Exercise 14: Use Assignment Operators

# 1. Start with the initial value
initial_value = 10

print(f"Initial Value: {initial_value}")

# 2. Addition Assignment: 10 + 5 = 15
initial_value += 5
print(f"After += : {initial_value}")

# 3. Subtraction Assignment: 15 - 3 = 12
initial_value -= 3
print(f"After -= : {initial_value}")
# 4. Multiplication Assignment: 12 * 2 = 24
initial_value *= 2
print(f"After *= : {initial_value}")

# 5. Division Assignment: 24 / 3 = 8.0
initial_value /= 3
print(f"After /= : {initial_value}")

# 6. Modulus Assignment: 8.0 % 2 = 0.0 (The remainder of 8.0 divided by 2 is 0.0)
initial_value %= 2
print(f"After %= : {initial_value}")