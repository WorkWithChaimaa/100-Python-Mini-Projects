# Exercise 12: Use Comparison Operators

# 1. Define two numbers to be used for all operations
num1 = 10
num2 = 5
num3 = 10 # Used specifically for the equality check (==)

# 2. print the results for each comparison operation
print(f"{num1} > {num2}: {num1 > num2}")

print(f"{num1} < {num2}: {num1 < num2}")

print(f"{num1} == {num3}: {num1 == num3}") # Compares 10 to 10

print(f"{num1} != {num2}: {num1 != num2}")

print(f"{num1} >= {num2}: {num1 >= num2}")

print(f"{num1} <= {num2}: {num1 <= num2}")
