# Exercise 11: Perform Arithmetic Operations

# 1. Define two numbers to be used for all operations
num1 = 10
num2 = 5

# 2. Perform and print the results for each arithmetic operation

# Addition: num1 + num2
addition_result = num1 + num2
print(f"Addition: {addition_result}")

# Subtraction: num1 - num2
subtraction_result = num1 - num2
print(f"Subtraction: {subtraction_result}")

# Multiplication: num1 * num2
multiplication_result = num1 * num2
print(f"Multiplication: {multiplication_result}")

# Division: num1 / num2
# Note: Standard division always produces a float in Python.
division_result = num1 / num2
print(f"Division: {division_result}")

# Modulus: num1 % num2
modulus_result = num1 % num2
print(f"Modulus: {modulus_result}")

