# Exercise 38: Store Student Grades in a Dictionary

# 1. Create the dictionary with student names (keys) and grades (values)
student_grades = {
    "James": "A",
    "Sarah": "A",
    "carolina": "B",
    "John": "B"
}

# 2. Retrieve the grade of "Sarah" and print the result
# The square bracket notation [key] retrieves the value.
print(f"Sarah's grade: {student_grades['Sarah']}")