# Exercise 10: Convert Dictionary Keys and Values to Lists

# 1. Declare the initial dictionary of keys and values
dictionary = {"name": "Lkhibra Academy",
             "age": 5,
             "Language": "Python"}

# 2. Convert Dictionary Keys into a List
keys_list = list(dictionary.keys())
print(f"Keys: {keys_list}")

# 3. Convert Dictionary Values into a List
values_list = list(dictionary.values())
print(f"values: {values_list}")